let init ~(filename: string) : Lexing.lexbuf =
  attribDepth := 0;
  init_lexicon ();
  (* Inititialize the pointer in Errormsg *)
  E.add_type := add_type;
  E.push_context := push_context;
  E.pop_context := pop_context;
  E.add_identifier := add_identifier;
  E.startParsing (E.ParseFile filename)


